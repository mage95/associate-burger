package Bean;

/**
 *
 * @author mage95
 */
public class Transactions {

    private int id;
    private int user_id;
    private String date;
    private String status;

    public Transactions(int id, int user_id, String date, String status) {
        this.id = id;
        this.user_id = user_id;
        this.date = date;
        this.status = status;
    }

    
    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getUser_id() {
        return user_id;
    }

    public void setUser_id(int user_id) {
        this.user_id = user_id;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }
    
    
}
